package iotdevice.com.iotDevice.model;

public class CustomerAddress {
    String city;
    String state;

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

}
